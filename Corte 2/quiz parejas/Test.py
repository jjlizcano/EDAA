minutos_transcurridos = 0 
ventas = 0
promedio = 0

for i in range(2): # primeras dos horas
    for i in range(6): # 6 ventas por hora
        minutos_transcurridos += 10 # se hace una venta cada 10 minutos

        if minutos_transcurridos > 36:
            break

        ventas += 1 
        valor_venta = int(input("Ingrese el valor de la factura: "))
        promedio += valor_venta
print("El valor total de las ventas en los primeros 36 minutos es: ", ventas)
print("La cantidad de ventas es: ", ventas)
print("El promedio de ventas es: ", promedio/ventas)


































